function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% 全局變數宣告
global img_filename;
global img_L1;
global img_L2;
% 開啟檔案選取交談視窗
[filename, pathname] = uigetfile( ...
    {'*.jpg;*.tif;*.png;*.gif','All Image Files';...
    '*.*','All Files' },...
    '請選取要測試的圖片', ...
    fullfile(pwd, 'test', 'Test1.jpg'));
% 若果取消選取，則不進行動作
if isequal(filename, 0) || isequal(pathname, 0)
    return;
end
% 顯示選取的檔案
fprintf('file=%s%s\r\n',pathname,filename);
% 產生檔案路徑
img_filename = fullfile(pathname, filename);
% 讀取
img = imread(img_filename);
% Clear
InitAxes(handles);
pause(0.1);

% 呼叫函數
hw = waitbar(0.5,'Please wait...');
[img_L1,img_L2] = Main_Process(img_filename);
close(hw);

% 顯示
axes(handles.axes1); imshow(img, []);title('原始圖形');
axes(handles.axes8); imshow(img_L1, []);title('重構圖形【RRC L1方法】');
axes(handles.axes9); imshow(img_L2, []);title('重構圖形【RRC L2方法】');


